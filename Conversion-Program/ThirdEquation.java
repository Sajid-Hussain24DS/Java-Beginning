class ThirdEquation { 
public static void main(String[] args) {

// Third equation of motion > v^2 = u^2 + 2as

int initialVelocity = 10^2;
int variable = 2;
int acceleration = 4;
int displacement = 8;
int finalVelocity = initialVelocity + variable * acceleration * displacement;
System.out.println("The value of final velocity is" + finalVelocity);



}

}