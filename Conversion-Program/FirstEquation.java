class FirstEquation{
 
public static void main(String[] args) {
// first equation of motion
// v  = u + at

int initialVelocity = 25;
int acceleration = 10;
int time = 20;
int finalVelocity = initialVelocity + acceleration * time;
System.out.println("The value of final velocity" + finalVelocity);



}

}